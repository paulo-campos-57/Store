public class Compra {

	private Produto produtoCompra;
	private String codCompra;
	private String notaFiscal;
	private String codProCompra;
	private String qtdCompra;
	private String codForCompra;
	private String dataCompra;
	
	public Produto getProdutoCompra() {
		return produtoCompra;
	}
	
	public void setProdutoCompra(Produto produtoCompra) {
		this.produtoCompra = produtoCompra;
	}
	
	public String getCodCompra() {
		return codCompra;
	}
	
	public void setCodCompra(String codCompra) {
		this.codCompra = codCompra;
	}
	
	public String getNotaFiscal() {
		return notaFiscal;
	}
	
	public void setNotaFiscal(String notaFiscal) {
		this.notaFiscal = notaFiscal;
	}
	
	public String getCodProCompra() {
		return codProCompra;
	}
	
	public void setCodProCompra(String codProCompra) {
		this.codProCompra = codProCompra;
	}
	
	public String getQtdCompra() {
		return qtdCompra;
	}
	
	public void setQtdCompra(String qtdCompra) {
		this.qtdCompra = qtdCompra;
	}
	
	public String getCodForCompra() {
		return codForCompra;
	}
	
	public void setCodForCompra(String codForCompra) {
		this.codForCompra = codForCompra;
	}
	
	public String getDataCompra() {
		return dataCompra;
	}
	
	public void setDataCompra(String dataCompra) {
		this.dataCompra = dataCompra;
	}
}
